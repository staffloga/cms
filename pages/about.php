    <h1>О CMS</h1>
    <p>Система управления контентом (CMS) — это программное обеспечение, которое помогает пользователям создавать, управлять и изменять содержимое на веб-сайте без необходимости специализированных технических знаний. CMS используются для управления текстовым и мультимедийным контентом, а также для автоматизации процессов публикации.</p>

    <h2>История создания CMS</h2>
    <p>Первые CMS появились в конце 1990-х годов, когда веб-сайты стали более сложными, и потребовались инструменты для упрощения управления контентом. Одной из первых CMS была Vignette StoryServer, выпущенная в 1995 году. С тех пор CMS эволюционировали, и сегодня существует множество платформ, таких как WordPress, Joomla и Drupal.</p>

